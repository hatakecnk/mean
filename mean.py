import statistics
 
data = [37, 48, 52, 59, 68, 74, 80]
 
# Finding Mean
print("\nMean: ", statistics.mean(data))
 
# Finding Median
print("Median: ", statistics.median(data))
 
# Finding Single Mode
print("Single Mode: ", statistics.mode(data))
 
# Finding Multiple Modes
print("Mode: ", statistics.multimode(data))
